package principal;

public class PesoAlturaC {

    private double peso;
    private int altura;

    public PesoAlturaC(double peso, int altura) {
        this.altura = altura;
        this.peso = peso;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
}
